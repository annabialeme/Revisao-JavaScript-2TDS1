// Se a altura da pessoa for maior ou igual a altura minima e menor que altura maxima : Permitido, senão: Não permitido

let alturaPessoa = 1.63;
let alturaMinima =  1.40;
let alturaMaxima = 2.01;

if (alturaPessoa >= alturaMinima && alturaPessoa < alturaMaxima) {
console.log("Permitido!");
} else {
    console.log("Não Permitido!");
}